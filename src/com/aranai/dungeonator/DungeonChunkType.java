/**
 * Specifies types for DungeonChunks
 * @see DungeonChunk
 */
public enum DungeonChunkType {
	
	/** A basic tile chunk. */
	BASIC_TILE,
	
	/** A set tile chunk. */
	SET_TILE,
	
	/** A procedurally generated chunk. */
	PROCEDURAL,
	
	/** A biome chunk. */
	BIOME
}
